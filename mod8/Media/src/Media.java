public class Media {

    public static void main(String[] args) {
        int nota1 = 10;
        int nota2 = 8;
        int nota3 = 6;
        int nota4 = 1;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("******* Notas de fulano ******* ");
        System.out.println("Nota da prova 1: " + nota1);
        System.out.println("Nota da prova 2: " + nota2);
        System.out.println("Nota da prova 3: " + nota3);
        System.out.println("Nota da prova 4: " + nota4);
        System.out.println("A média das notas é igual a " + media);

    }
}
